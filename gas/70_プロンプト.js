/* =========================================================
 * 自由質問用（doPost から呼び出し）
 * p: { userLevel, settings: { 事例数, 最大行数, 専門度 }, reviewText, coachCheckText, recentQA, questionText, datasetDocs }
 * =======================================================*/

function プロンプト_自由質問_(p) {
  const userLevel = p.userLevel || "未設定";
  const settings = p.settings || { 事例数: 4, 最大行数: 7, 専門度: "標準" };

  const candidates = (p.datasetDocs || []).map((d, i) => {
    const rt = d?.fields?.["解析結果テキスト"]?.stringValue || "";
    const cc = d?.fields?.["コーチ観察テキスト"]?.stringValue || "";
    const sq = d?.fields?.sourceVideoMessageId?.stringValue || "";
    return [
      `候補${i + 1}`,
      `videoId=${sq}`,
      (`解析結果: ${rt}`).slice(0, 700),
      cc ? (`観察: ${cc}`).slice(0, 700) : ""
    ].filter(Boolean).join("\n");
  }).join("\n\n");

  const lines = [
    "あなたはプロのゴルフコーチです。",
    `ユーザーレベル：${userLevel}（回答の専門性：${settings.専門度}）`,
    "",
    "以下の情報を根拠に回答してください。推測は禁止。",
    "",
    "【直前レビュー（総評）】",
    String(p.reviewText || ""),
    "",
    "【P1〜P10観察メモ】",
    String(p.coachCheckText || ""),
    "",
    "【この動画の直近Q&A】",
    String(p.recentQA || ""),
    "",
    "【データセット候補】",
    "必要だと判断した場合のみ、候補を最大" + settings.事例数 + "件だけ使ってよい。",
    "回答の最後の行に必ず「参考:候補1」〜「参考:候補" + settings.事例数 + "」のどれかを1つだけ書く。英字（Aなど）や追加文字は禁止。",
    "",
    candidates,
    "",
    "【ユーザーの質問】",
    String(p.questionText || ""),
    "",
    "出力制約：",
    `・最大${settings.最大行数}行（改行区切り）`,
    "・結論→理由→具体ドリル（あれば）",
    "・前置き禁止",
  ];
  return lines.join("\n");
}

