/** @deprecated v2で廃止。単に連結するだけ */
function プロンプト_結合_(systemText, userText) {
  const s = String(systemText || "").trim();
  const u = String(userText || "").trim();
  if (!s) return u;
  return s + "\n\n---\n\n" + u;
}

